import java.util.Scanner;
public class MultiplicationTable{

public static void main (String[] args) {

    for (int i = 1; i <= 20; i++){
        
        for (int j = 1; j <= 20; j++){
            System.out.print(i * j + "\t ");
            }
            
        System.out.println();
        }
   } // end of main
}



    